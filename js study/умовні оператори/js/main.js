// умовні оператори

let userMinAge = 18;

// if else operators

if (userMinAge === 18) {
    alert("Welcome to this page!")
}
else {
    alert("You don't have roots for this page!");
}


// ternary operator

let userMaxAge = 60;

userMaxAge ? alert("Welcome to this page :)") : alert("You very old for this page");



// switch case operators

let age = 30;

switch (age) {
    case 10: alert("Give me your kiss");
        break;
    case 20: alert("Give me your hug");
        break;
    case 30: alert("Give me your money");
        break;
    default:
        alert("Give me your love");
}